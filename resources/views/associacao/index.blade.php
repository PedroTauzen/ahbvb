<section id="sobre" class="max-w-5xl mx-auto px-4 py-16">
    <h2 class="text-3xl font-bold text-red-700 mb-6">Sobre a Associação</h2>

    <img src="/images/sede.jpg" alt="Sede da Associação" class="rounded-xl shadow-md mb-6 w-full max-h-96 object-cover">

    <p class="mb-4">
        A <strong>Associação Humanitária dos Bombeiros Voluntários de Beja (AHBVB)</strong> é uma instituição centenária fundada a <strong>12 de maio de 1889</strong>,
        resultado da união e coragem de um grupo de homens determinados em servir a sua comunidade. Com sede na cidade de Beja, é uma Pessoa Coletiva de Utilidade Pública
        cuja atuação vai muito além da intervenção em incêndios.
    </p>

    <p class="mb-4">
        A AHBVB mantém em atividade um Corpo de Bombeiros composto por cerca de <strong>90 operacionais e 45 funcionários</strong> — muitos dos quais acumulam funções
        administrativas com o voluntariado ativo —, assegurando uma resposta eficaz e contínua às emergências no concelho de Beja, que cobre uma área de <strong>1.146,5 km²</strong>,
        abrangendo as suas 18 freguesias.
    </p>

    <p class="mb-4">
        A sua atividade principal centra-se na <strong>proteção de pessoas e bens</strong>, com intervenção em incêndios florestais e urbanos, acidentes rodoviários, inundações,
        secas (através de abastecimento de água) e todos os eventos que coloquem em risco a segurança da população. Paralelamente, presta <strong>apoio social contínuo</strong>,
        especialmente à população idosa, assegurando:
    </p>

    <ul class="list-disc list-inside mb-4">
        <li><strong>20% de desconto</strong> nos transportes de ambulância para sócios;</li>
        <li><strong>Transporte diário</strong> de utentes para consultas e tratamentos, mesmo fora do concelho;</li>
        <li><strong>Cuidados de enfermagem gratuitos</strong>, duas vezes por semana na sede da Associação;</li>
        <li><strong>Apoio solidário</strong> em casos urgentes, com serviço de transporte ativo <strong>365 dias por ano, 24h por dia</strong>.</li>
    </ul>

    <p class="mb-4">
        Enquanto organização, a Associação desenvolve também atividades de <strong>cariz associativo, cultural, desportivo e formativo</strong>, promovendo o
        <strong>associativismo e o voluntariado</strong> como pilares fundamentais da sua missão. A sua ação é movida por valores como a <strong>solidariedade, o altruísmo,
            o serviço público</strong> e o <strong>compromisso com a comunidade</strong>.
    </p>

    <p class="mb-4">
        Atualmente, a Associação conta com cerca de <strong>2.000 sócios</strong>, a maioria idosos, que contribuem com uma <strong>quota simbólica mínima mensal de 1€</strong>.
        O apoio dos sócios é essencial para a manutenção dos serviços, das viaturas, das instalações e das atividades desenvolvidas.
    </p>

    <p class="text-sm text-gray-600 italic">“Vida por Vida” — Um lema que se vive, não apenas se diz.</p>
</section>

<section id="historia" class="max-w-5xl mx-auto px-4 py-16">
    <h2 class="text-3xl font-bold text-red-700 mb-6">História</h2>

    <img src="/images/historia.jpg" alt="Imagem histórica dos Bombeiros Voluntários de Beja" class="rounded-xl shadow-md mb-6 w-full max-h-96 object-cover">

    <p class="mb-4">
        A história dos <strong>Bombeiros Voluntários de Beja</strong> remonta a <strong>12 de maio de 1889</strong>, quando, impulsionados por um forte espírito de
        boa vontade e solidariedade, um grupo de cidadãos da cidade decidiu fundar esta associação para dar resposta às necessidades urgentes da população local.
    </p>

    <p class="mb-4">
        Com mais de <strong>130 anos de existência</strong>, a AHBVB atravessou gerações, conflitos, dificuldades económicas e crises sociais, mas manteve-se sempre
        firme no seu propósito de servir. Comemorando em 2018 os seus 129 anos, a Direção homenageou o passado e reafirmou a missão da Associação:
        <strong>continuar a "fazer o bem sem olhar a quem"</strong>.
    </p>

    <p class="mb-4">
        Ao longo dos tempos, a sua intervenção alargou-se para além da cidade, apoiando outros concelhos do distrito de Beja, regiões do continente e, sempre que
        necessário, regiões autónomas. A sua história está marcada por inúmeras <strong>alegrias e vitórias</strong>, mas também por <strong>sacrifícios e superação</strong>,
        sempre com o objetivo de prestar o melhor apoio possível à população.
    </p>

    <p class="mb-4">
        A frase <strong>“Ajude-nos a ajudá-lo”</strong> não é apenas um slogan, mas um apelo que representa a essência da causa humanitária que move esta instituição.
        Orgulhosa do seu legado, a Associação olha para o futuro com o compromisso de <strong>honrar os seus fundadores, reforçar o voluntariado jovem</strong> e manter viva
        a tradição de <strong>serviço, coragem e humanidade</strong>.
    </p>
</section>

